<h2><a name="db">База данных</a></h2>

<h3><a name="db_common">Общие сведения</a></h3>

<p>В базе данных портфолио хранятся в следующих таблицах:</p>

<ul>
<li>ksh_portfolio_categories - категории портфолио;</li>
<li>ksh_portfolio - сами портфолио.</li>
</ul>

<p>На диске элементы, принадлежащие к портфолио, хранятся в каталоге /uploads/portfolio.</p>

