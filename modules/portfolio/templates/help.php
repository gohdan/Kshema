<h2><a name="portfolio">Потфолио</a></h2>

<p>К портфолио можно прикреплять изображения.</p>

<h3><a name="portfolio_common">Общие сведения</a></h3>

<p>Портфолио - небольшие публикации. Они имеют некоторые общие характеристики - дату и изображение-описание, 
которое может быть показано в списке портфолио.</p>

<h3><a name="portfolio_add">Добавление</a></h3>

<ul>
<li>Название может содержать любые символы. Оно будет показано пользователю в списке портфолио и т. п.</li>
<li>Дата - должна быть в формате ГГГГ-ММ-ДД.</li>
<li>Категория - каждое портфолио относится к какой-либо категории.</li>
<li>Текст - может содержать любые символы, HTML-код, может заполняться с помощью визуального редактора.</li>
</ul>

