<h1>Закачки - справка</h1>

<p>
<a href="/uploads/admin/">Обратно к администрированию</a>
</p>

<p>
Модуль позволяет загрузить на сервер файл или изображение для последующего использования.
После загрузки он предоставляет прямую ссылку на закачанный файл, которую потом нужно вставить в публикацию.
Закачанные файлы сохраняются в каталоге /uploads/.
</p>
